package com.example.contact;

public class Item {
    private String ten;
    private String so;

    public Item() {
    }

    public Item(String ten, String so) {
        this.ten = ten;
        this.so = so;
    }


    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getSo() {
        return so;
    }

    public void setSo(String so) {
        this.so = so;
    }
}
